const result1 = '5' == 5; // so sánh giá trị
const result2 = '5' === 5; // so sánh giá trị và kiểu dữ liệu

console.log(result1); // true
console.log(result2); // false 

const result3 = 5 != 5; // so sánh giá trị khác nhau
const result4 = 5 !== 5; // so sánh giá trị và kiểu dữ liệu khác nhau

console.log(result3); // false
console.log(result4); // false